    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'banner_url' => 'required|image|mimes:jpeg,png,jpg,gif,svg'
        ]);
        
        $course = new Course;
        $course->category_id = $request->category_id;
        $course->name = $request->name;
        $course->short_description = $request->short_description;
        $course->description = $request->long_description;
        $course->video_url = $request->video_url;
        
        $bannerUrlPath = $request->file('banner_url')->store('public/images/course');
        $course->banner_url = $bannerUrlPath;

        $course->save();

        return redirect('admin/course');
    }


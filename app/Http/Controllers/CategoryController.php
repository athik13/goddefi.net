    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'banner_url' => 'required|image|mimes:jpeg,png,jpg,gif,svg',
            'icon_url' => 'required|image|mimes:jpeg,png,jpg,gif,svg',
        ]);

        $category = new Category;
        $category->name = $request->name;

        $bannerUrlPath = $request->file('banner_url')->store('public/images');
        $iconUrlPath = $request->file('icon_url')->store('public/images');
        
        $category->banner_url = $bannerUrlPath;
        $category->icon_url = $iconUrlPath;

        $category->save();

        return redirect('admin/category');
    }


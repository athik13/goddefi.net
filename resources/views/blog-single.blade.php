                    <p>Hello, Market Wrap readers! During the final two weeks of 2021 we're using this space to recap the year's most dramatic moments in cryptocurrency markets and highlight key lessons from this fast-evolving corner of global finance. Over a series of eight posts starting on Dec. 20 and running through Dec. 30, we're recapping what shook crypto markets this year. (For the latest crypto prices and news headlines, please scroll down.)</p>
                    <p>On Monday, we recounted how even as bitcoin staged a powerful price rally to the start of the year, some institutional investors began to question the sustainability of the trend. Today, we'll show how, during January and February, coordination and posts on social media fueled even more demand for bitcoin and other cryptocurrencies, delaying an immediate price correction.</p>
                    <p>As bitcoin (BTC) soared in February, social media, particularly Twitter, appeared to take on an expanded role in cryptocurrency markets, with prices pumping in response to tweet after tweet. It became clear that investor appetite for risk remained strong despite earlier concerns about rampant speculation.</p>
                    <p>Tesla CEO Elon Musk and then-Twitter CEO Jack Dorsey tweeted away to push bitcoin higher from $40,000 in January to nearly $57,000 in February. The viral effects of social media pushed retail traders into full-on buy mode, with bones thrown to the doggy-themed joke token dogecoin (DOGE) that added billions of dollars to that cryptocurrency's market value.</p>
                    <p>On social media, some traders banded together in an effort to keep crypto prices elevated similar to the way retail traders in traditional markets had coordinated to roil stocks like GameStop.</p>
                    <p>For example, Musk, ranked by Forbes as the world's richest person, added the #Bitcoin hashtag to his Twitter profile, contributing to an immediate 11% BTC price rally. Shortly afterward, Jack Dorsey also added the #Bitcoin hashtag to his twitter profile.</p>

                    <p>The chart below tracks Musk's influence on the DOGE price over time, using data from TradingView.</p>
                    <img alt="" src="https://cloudfront-us-east-1.images.arcpublishing.com/coindesk/KGL2L46TCBEKZB357N56JSJ2JI.png" class="img-fullwidth rounded">

                    <p>During bitcoin's swift ascent in January and February, retail traders used social media as a gateway to discover new alternative cryptocurrencies and react to market sentiment in real time.</p>
                    <p>In just a few tweets, Musk and other popular figures were able to pump and dump coins, leading to significant price gains and losses.</p>
                    <p>The lesson of this ever-so-bizarre stretch of crypto markets history is that social media was, and still is, a force that's impossible for traders to ignore.</p>
        
